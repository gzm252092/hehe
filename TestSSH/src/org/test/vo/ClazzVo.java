package org.test.vo;

public class ClazzVo {
	String claid;
	String name;
	
	
	public ClazzVo(String claid, String name) {
		this.claid = claid;
		this.name = name;
	}
	public String getClaid() {
		return claid;
	}
	public void setClaid(String claid) {
		this.claid = claid;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
